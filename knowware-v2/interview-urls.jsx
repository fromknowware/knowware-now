function interviewUrl(n) {
  const slug = INTERVIEW_SLUGS[n];
  if (!slug) return null;
  // All filenames have a leading space in the repo — encode it
  const parts = slug.split('/');
  parts[parts.length - 1] = '%20' + parts[parts.length - 1];
  return BASE + parts.join('/');
}

